#!/usr/bin/env python3
"""
Read per-frame, per-object RGB/thermal trajectory points from the extracted CSV.

Input:
  outputs/vt_tiny_mot_trajectories/observations_train.csv

Each yielded data point is sorted by time within one (sequence, track_id) trajectory:
  {
    "sequence": "...",
    "track_id": "...",
    "frame_id": 13,
    "rgb": {...},
    "thermal": {...},
    "modal": {...}
  }
"""

from __future__ import annotations

import argparse
import csv
import json
from collections import defaultdict
from pathlib import Path
from typing import Any, Iterable


DEFAULT_CSV = (
    Path("outputs")
    / "vt_tiny_mot_trajectories"
    / "observations_train.csv"
)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Read sorted RGB/thermal trajectory data points."
    )
    parser.add_argument(
        "--csv-path",
        type=Path,
        default=DEFAULT_CSV,
        help="Path to observations_<split>.csv generated by extract_vt_tiny_mot_trajectories.py.",
    )
    parser.add_argument(
        "--sequence",
        default=None,
        help="Optional sequence filter, for example DJI_0022_1.",
    )
    parser.add_argument(
        "--track-id",
        default=None,
        help="Optional track id filter, for example 4700000.",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=10,
        help="Maximum number of data points to print. Use 0 to print all.",
    )
    parser.add_argument(
        "--output-jsonl",
        type=Path,
        default=None,
        help="Optional path to write sorted data points as JSONL.",
    )
    return parser.parse_args()


def parse_float(value: str) -> float | None:
    if value == "" or value is None:
        return None
    return float(value)


def parse_int(value: str) -> int | None:
    if value == "" or value is None:
        return None
    return int(float(value))


def parse_bbox(row: dict[str, str], prefix: str) -> list[float] | None:
    keys = [f"{prefix}_x", f"{prefix}_y", f"{prefix}_w", f"{prefix}_h"]
    values = [parse_float(row[key]) for key in keys]
    if any(value is None for value in values):
        return None
    return [float(value) for value in values]


def parse_center(row: dict[str, str], prefix: str) -> list[float] | None:
    cx = parse_float(row[f"{prefix}_cx"])
    cy = parse_float(row[f"{prefix}_cy"])
    if cx is None or cy is None:
        return None
    return [cx, cy]


def modality_state(row: dict[str, str], prefix: str) -> dict[str, Any] | None:
    bbox = parse_bbox(row, prefix)
    center = parse_center(row, prefix)
    if bbox is None or center is None:
        return None

    return {
        "file": row.get(f"{prefix}_file") or None,
        "bbox_xywh": bbox,
        "center_xy": center,
        "velocity_px_per_frame": [
            parse_float(row[f"{prefix}_vx_px_per_frame"]),
            parse_float(row[f"{prefix}_vy_px_per_frame"]),
        ],
        "speed_px_per_frame": parse_float(row[f"{prefix}_speed_px_per_frame"]),
        "velocity_px_per_second": [
            parse_float(row[f"{prefix}_vx_px_per_second"]),
            parse_float(row[f"{prefix}_vy_px_per_second"]),
        ],
        "speed_px_per_second": parse_float(row[f"{prefix}_speed_px_per_second"]),
    }


def row_to_data_point(row: dict[str, str]) -> dict[str, Any]:
    return {
        "sequence": row["sequence"],
        "track_id": row["track_id"],
        "category_id": parse_int(row["category_id"]),
        "category_name": row["category_name"],
        "fps": parse_float(row["fps"]),
        "frame_id": parse_int(row["frame_id"]),
        "rgb": modality_state(row, "rgb"),
        "thermal": modality_state(row, "thermal"),
        "modal": {
            "offset_dx_thermal_minus_rgb": parse_float(
                row["modal_offset_dx_thermal_minus_rgb"]
            ),
            "offset_dy_thermal_minus_rgb": parse_float(
                row["modal_offset_dy_thermal_minus_rgb"]
            ),
            "offset_distance": parse_float(row["modal_offset_distance"]),
            "bbox_iou": parse_float(row["modal_bbox_iou"]),
        },
    }


def load_sorted_trajectories(
    csv_path: Path,
    sequence: str | None = None,
    track_id: str | None = None,
) -> dict[tuple[str, str], list[dict[str, Any]]]:
    """Load data points, grouped by object trajectory and sorted by frame_id."""
    trajectories: dict[tuple[str, str], list[dict[str, Any]]] = defaultdict(list)

    with csv_path.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            if sequence is not None and row["sequence"] != sequence:
                continue
            if track_id is not None and row["track_id"] != track_id:
                continue

            point = row_to_data_point(row)
            key = (point["sequence"], point["track_id"])
            trajectories[key].append(point)

    for points in trajectories.values():
        points.sort(key=lambda item: item["frame_id"])

    return dict(sorted(trajectories.items(), key=lambda item: item[0]))


def iter_sorted_data_points(
    csv_path: Path,
    sequence: str | None = None,
    track_id: str | None = None,
) -> Iterable[dict[str, Any]]:
    """Yield every data point in trajectory order, with time sorted inside each track."""
    trajectories = load_sorted_trajectories(csv_path, sequence, track_id)
    for _, points in trajectories.items():
        yield from points


def main() -> None:
    args = parse_args()
    csv_path = args.csv_path.resolve()
    points = list(iter_sorted_data_points(csv_path, args.sequence, args.track_id))

    if args.output_jsonl is not None:
        output_path = args.output_jsonl.resolve()
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with output_path.open("w", encoding="utf-8") as f:
            for point in points:
                f.write(json.dumps(point, ensure_ascii=False))
                f.write("\n")
        print(f"Wrote {len(points)} sorted data points to {output_path}")

    limit = len(points) if args.limit == 0 else min(args.limit, len(points))
    for point in points[:limit]:
        print(json.dumps(point, ensure_ascii=False))

    if args.limit and len(points) > limit:
        print(f"... {len(points) - limit} more sorted data points")


if __name__ == "__main__":
    main()
